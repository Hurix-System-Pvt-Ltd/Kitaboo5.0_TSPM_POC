package com.hurix.reader.kitaboosdkrenderer.ThemeParser;

public class SearchResultCard {

   private String background;
    private String titleTextColor;
    private String metadataTextColor;
    private String descriptionTextColor;
    private String thumbnailBackground;


    public String getBackground() {
        return background;
    }

    public void setBackground(String background) {
        this.background = background;
    }

    public String getTitleTextColor() {
        return titleTextColor;
    }

    public void setTitleTextColor(String titleTextColor) {
        this.titleTextColor = titleTextColor;
    }

    public String getMetadataTextColor() {
        return metadataTextColor;
    }

    public void setMetadataTextColor(String metadataTextColor) {
        this.metadataTextColor = metadataTextColor;
    }

    public String getDescriptionTextColor() {
        return descriptionTextColor;
    }

    public void setDescriptionTextColor(String descriptionTextColor) {
        this.descriptionTextColor = descriptionTextColor;
    }

    public String getThumbnailBackground() {
        return thumbnailBackground;
    }

    public void setThumbnailBackground(String thumbnailBackground) {
        this.thumbnailBackground = thumbnailBackground;
    }
}
