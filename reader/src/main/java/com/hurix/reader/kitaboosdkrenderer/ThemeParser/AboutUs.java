package com.hurix.reader.kitaboosdkrenderer.ThemeParser;

public class AboutUs {
    private String background;
    private String closeIconColor;
    private String textColor;
    private String descriptionTextColor;
    private String iconsColor;
    private String backgroundImage;
    private GradientColor gradientColor;
    private Svg svg;

    public String getBackground() {
        return background;
    }

    public void setBackground(String background) {
        this.background = background;
    }

    public String getCloseIconColor() {
        return closeIconColor;
    }

    public void setCloseIconColor(String closeIconColor) {
        this.closeIconColor = closeIconColor;
    }

    public String getTextColor() {
        return textColor;
    }

    public void setTextColor(String textColor) {
        this.textColor = textColor;
    }

    public String getDescriptionTextColor() {
        return descriptionTextColor;
    }

    public void setDescriptionTextColor(String descriptionTextColor) {
        this.descriptionTextColor = descriptionTextColor;
    }

    public String getIconsColor() {
        return iconsColor;
    }

    public void setIconsColor(String iconsColor) {
        this.iconsColor = iconsColor;
    }

    public GradientColor getGradientColor() {
        return gradientColor;
    }

    public void setGradientColor(GradientColor gradientColor) {
        this.gradientColor = gradientColor;
    }

    public Svg getSvg() {
        return svg;
    }

    public void setSvg(Svg svg) {
        this.svg = svg;
    }

    public String getBackgroundImage() {
        return backgroundImage;
    }

    public void setBackgroundImage(String backgroundImage) {
        this.backgroundImage = backgroundImage;
    }
}
