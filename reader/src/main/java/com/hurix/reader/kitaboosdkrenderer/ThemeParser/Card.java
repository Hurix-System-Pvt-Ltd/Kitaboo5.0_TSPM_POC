package com.hurix.reader.kitaboosdkrenderer.ThemeParser;

public class Card {

  private String background,titleTextColor,typeTextColor,iconsColor,downloadingBorder,thumbnailBackground;

    public String getBackground() {
        return background;
    }

    public void setBackground(String background) {
        this.background = background;
    }

    public String getTitleTextColor() {
        return titleTextColor;
    }

    public void setTitleTextColor(String titleTextColor) {
        this.titleTextColor = titleTextColor;
    }

    public String getTypeTextColor() {
        return typeTextColor;
    }

    public void setTypeTextColor(String typeTextColor) {
        this.typeTextColor = typeTextColor;
    }

    public String getIconsColor() {
        return iconsColor;
    }

    public void setIconsColor(String iconsColor) {
        this.iconsColor = iconsColor;
    }

    public String getDownloadingBorder() {
        return downloadingBorder;
    }

    public void setDownloadingBorder(String downloadingBorder) {
        this.downloadingBorder = downloadingBorder;
    }

    public String getThumbnailBackground() {
        return thumbnailBackground;
    }

    public void setThumbnailBackground(String thumbnailBackground) {
        this.thumbnailBackground = thumbnailBackground;
    }
}
